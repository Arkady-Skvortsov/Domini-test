import { ApiProperty } from '@nestjs/swagger';
import {
  Entity,
  PrimaryGeneratedColumn,
  Column,
  OneToOne,
  OneToMany,
} from 'typeorm';
import JwtTokenEntity from 'src/jwt-token/entities/jwt-token.entity';
import FriendsEntity from './friends.entity';

@Entity({ name: 'users' })
export default class UserEntity {
  @ApiProperty({
    type: Number,
    example: 23,
    description: 'Primary autogenerated key of the table',
  })
  @PrimaryGeneratedColumn()
  public id: number;

  @ApiProperty({
    type: String,
    example: 'Arkadiy',
    description: 'Username of the current user',
  })
  @Column({ type: 'varchar', nullable: false })
  public username: string;

  @ApiProperty({
    type: String,
    example: 'Arkasha.jpg',
    description: 'Photo of the current user',
  })
  @Column({ type: 'varchar', nullable: false })
  public photo: string;

  @ApiProperty({
    type: () => JwtTokenEntity,
    example: 'U73@$fghlOp234',
    description: 'JWT Refresh Token',
  })
  @OneToOne(() => JwtTokenEntity, (jwt) => jwt.token)
  public refreshToken: JwtTokenEntity;

  @ApiProperty({
    type: Number,
    example: 2000,
    description: 'Coins of the current user',
  })
  @Column({ type: 'bigint', default: 0 })
  public coins: number;

  @ApiProperty({
    type: () => FriendsEntity,
    example: 'Ilya, Ilshat, Khamil',
    description: 'Friends of the current user',
  })
  @OneToMany(() => FriendsEntity, (friends) => friends.user)
  public friends: FriendsEntity[];
}
